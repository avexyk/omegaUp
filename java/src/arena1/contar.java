
package arena1;

import java.util.Scanner;

public class contar {
    
    public static void main(String[] args) {
        
        Scanner in = new Scanner(System.in);
        int N;
        
        N = in.nextInt();
        
        for (int i = 0; i < N; i++) {
            System.out.print((i+1) + " ");
        }
        
    }
    
}
